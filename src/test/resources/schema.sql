-- Simulate oracle database structure in H2 database
-- H2 System tables http://www.h2database.com/html/systemtables.html#information_schema
-- ORACLE System tables https://docs.oracle.com/database/timesten-18.1/TTSYS/systemtables.htm#TTSYS716
CREATE USER SYS PASSWORD 'PASSWORD';
GRANT ALTER ANY SCHEMA TO SYS;
CREATE SCHEMA SYS AUTHORIZATION SYS;

CREATE VIEW ALL_TABLES AS SELECT * FROM INFORMATION_SCHEMA.TABLES;
CREATE VIEW SYS.DBA_USERS AS SELECT SCHEMA_NAME AS USERNAME FROM INFORMATION_SCHEMA.SCHEMATA;
CREATE VIEW USER_TAB_COLS AS SELECT COLUMN_NAME, TYPE_NAME AS DATA_TYPE, TABLE_NAME FROM INFORMATION_SCHEMA.COLUMNS;
CREATE VIEW ALL_CONSTRAINTS AS SELECT * FROM INFORMATION_SCHEMA.CONSTRAINTS;