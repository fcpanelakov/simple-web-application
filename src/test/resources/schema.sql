DROP TABLE CONNECTIONS IF EXISTS;
CREATE TABLE CONNECTIONS
(
    "ID"              VARCHAR(120 CHAR) NOT NULL,
    "CONNECTION_NAME" VARCHAR(120 CHAR) NOT NULL,
    "URL"             VARCHAR(120 CHAR) NOT NULL,
    "USER_NAME"       VARCHAR(120 CHAR) NOT NULL,
    "PASSWORD"        VARCHAR(120 CHAR) NOT NULL,
    CONSTRAINT CONNECTIONS_PK PRIMARY KEY ("ID")
);

